      <h2><strong>Dynamics of Magnetic Nanorods in Non-Newtonian Fluids</strong></h2>

      <p>
      Magnetic nanorods have recently become a contender for addressing the challenges of
      in situ characterization of rheological properties of materials. Rheology can be
      defined as the study of a materialâ€™s flow which determined elastic and viscous properties of
      the material. Traditional rheometers cannot always appropriately measure properties for materials in
      the nanophase, hence where magnetic nanorods come into play. Potential uses for magnetic nanorods
      include fields such as medical, optofluidic, aerospace, and rheological engineering.<br>
      The simulation model produced mimics the work done by Aprevel et al. where nickel and cobalt
      nanorods are produced and a magnetic field is applied to rotate the rod. ESPResSo Molecular
      Dynamics software was used for modeling of nanorod rotation. This software uses the Lattice
      Boltzmann (LB) method for hydrodynamics of in-compressible fluids. This method is popular in
      fluid simulation because of its ability to handle constraint geometries in a simplified manner.
      Additionally, the software uses molecule dynamics resp. Lennard-Jones units internally which need to
      be matched to physical quantities. Scripts were written in TCL (Tool Command Language) that is
      compatible with ESPResSo.<br><br>
